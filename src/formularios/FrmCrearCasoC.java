    private void limpiar(){
        TfFecha.setText(null);
        TfHonorarios.setText(null);
        TfDescripcion.setText(null);
        TfPoderGeneralJudicial.setText(null);
        TfFecha.requestFocus();
    }
    
    private void mostrarEnTF(int fila){
        if(!casoCivil.getLista().isEmpty()){
            caso = casoCivil.getLista().get(fila).getTipoCaso();
            TfEstado.setText(casoCivil.getLista().get(fila).getEstadoCaso());
            TfDescripcion.setText(casoCivil.getLista().get(fila).getDescripcionProblema());
            TfFecha.setText(String.valueOf(casoCivil.getLista().get(fila).getFecha()));
            TfHonorarios.setText(String.valueOf(casoCivil.getLista().get(fila).getHonorarios()));
            TfPoderGeneralJudicial.setText(casoCivil.getLista().get(fila).getPoderGeneralJudicial());
            idCaso = casoCivil.getLista().get(fila).getId_caso();
            esNuevo = false;
            
            int ultReg = casoCivil.getLista().size();
            LbCantidad.setText("" + (fila + 1) + " de " + ultReg);
            TfFecha.requestFocus();
    
    
        }else{
            limpiar();
        
        }
    
    
    }
