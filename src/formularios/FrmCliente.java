    private void mostrarRegEnTf(int fila){
        if(!cliente.getLista().isEmpty()){
            TfCedula.setText(cliente.getLista().get(fila).getCedula());
            TfNombres.setText(cliente.getLista().get(fila).getNombres());
            TfApellidos.setText(cliente.getLista().get(fila).getApellidos());
            TfEdad.setText(String.valueOf(cliente.getLista().get(fila).getEdad()));
            TfDireccion.setText(cliente.getLista().get(fila).getDireccionDomicilio());
            TfSexo.setText(cliente.getLista().get(fila).getSexo());
            TfEstadoCivil.setText(cliente.getLista().get(fila).getEstadoCivil());
            TfProfesion.setText(cliente.getLista().get(fila).getProfesion());
            idCliente = cliente.getLista().get(fila).getCliente_id();
            esNuevo = false;
            
            int ultReg = cliente.getLista().size();
            LbRegistro.setText("" + (fila + 1) + " de " + ultReg);
            TfCedula.requestFocus();
        
        }else{
            limpiar();
        
        }
    
    
    }
