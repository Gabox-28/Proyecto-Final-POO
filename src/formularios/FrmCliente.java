    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        // TODO add your handling code here:
        if(TfCedula.getText().equals(null)){
            JOptionPane.showMessageDialog(this, "Debe ingresar el numero de cedula", "Cliente", JOptionPane.WARNING_MESSAGE);
            this.TfCedula.requestFocus();
            return;
        }
        if(TfNombres.getText().equals(null)){
            JOptionPane.showMessageDialog(this, "Debe ingresar los nomnbre", "Cliente", JOptionPane.WARNING_MESSAGE);
            this.TfNombres.requestFocus();
            return;
        }
        if(TfApellidos.getText().equals(null)){
            JOptionPane.showMessageDialog(this, "Debe ingresar los apellidos", "Cliente", JOptionPane.WARNING_MESSAGE);
            this.TfApellidos.requestFocus();
            return;
        }
        if(TfEdad.getText().equals(null)){
            JOptionPane.showMessageDialog(this, "Debe ingresar la edad", "Cliente", JOptionPane.WARNING_MESSAGE);
            this.TfEdad.requestFocus();
            return;
        }
        if(TfEstadoCivil.getText().equals(null)){
            JOptionPane.showMessageDialog(this, "Debe ingresar el estado civil", "Cliente", JOptionPane.WARNING_MESSAGE);
            this.TfEstadoCivil.requestFocus();
            return;
        }
        if(TfProfesion.getText().equals(null)){
            JOptionPane.showMessageDialog(this, "Debe ingresar la profesion", "Cliente", JOptionPane.WARNING_MESSAGE);
            this.TfProfesion.requestFocus();
            return;
        }
        if(TfSexo.equals(null)){
            JOptionPane.showMessageDialog(this, "Debe ingresar el sexo", "Cliente", JOptionPane.WARNING_MESSAGE);
            this.TfSexo.requestFocus();
            return;
        }
        if(TfDireccion.getText().equals(null)){
            JOptionPane.showMessageDialog(this, "Debe ingresar la dirección del domicilio", "Cliente", JOptionPane.WARNING_MESSAGE);
            this.TfDireccion.requestFocus();
            return;
        }
        
        int b = 0;
        if(esNuevo){
            b = cliente.agregarCliente(TfNombres.getText(), TfApellidos.getText(), TfDireccion.getText(), TfSexo.getText(), TfEstadoCivil.getText(), TfProfesion.getText(), Integer.parseInt(TfEdad.getText()), TfCedula.getText());
            
        }else{
        
            b = cliente.editarCliente(idCliente, TfNombres.getText(), TfApellidos.getText(), TfDireccion.getText(), TfSexo.getText(), TfEstadoCivil.getText(), TfProfesion.getText(), Integer.parseInt(TfEdad.getText()), TfCedula.getText());
           
        }
        
        if (b == 1){
            JOptionPane.showMessageDialog(this, "Registro guardado...",
                    "Cliente", JOptionPane.INFORMATION_MESSAGE);
            posActual = cliente.getLista().size();
            this.mostrarRegEnTf(posActual-1);
        }else{
            JOptionPane.showMessageDialog(this, "Error al guardar registro",
                    "Cliente", JOptionPane.ERROR_MESSAGE);
        }
        this.mostrarRegEnTf(0);
        limpiar();
    }//GEN-LAST:event_BtnGuardarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        // TODO add your handling code here:
        int resp = JOptionPane.showConfirmDialog(this, "¿Estas seguro de eliminar el registro de "  +
        this.TfNombres.getText() + "?", "Cliente", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(resp == 0){
            int b = cliente.eliminarCliente(idCliente);
            if(b == 1){
                JOptionPane.showMessageDialog(this, "El registro se elimino satisfactoriamente",
                        "Cliente", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Surgio un error al eliminar, "
                        + "por favor verifique si los datos proporcionado son correctos", "Cliente",
                        JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

    private void BtnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAnteriorActionPerformed
        // TODO add your handling code here:
        if(posActual == 0) posActual = cliente.getLista().size();
        posActual--;
        this.mostrarRegEnTf(posActual);
    }//GEN-LAST:event_BtnAnteriorActionPerformed

    private void BtnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSiguienteActionPerformed
        // TODO add your handling code here:
        posActual ++;
        if(posActual >= cliente.getLista().size()) posActual  = 0;
        this.mostrarRegEnTf(posActual);
    }//GEN-LAST:event_BtnSiguienteActionPerformed

